package pro2055

import (
	"testing"

	"github.com/bmizerany/assert"
)

func TestPlatesBetweenCandles(t *testing.T) {
	cases := []struct {
		s        string
		queries  [][]int
		expected []int
	}{
		{
			s:        "**|**|***|",
			queries:  [][]int{{2, 5}, {5, 9}},
			expected: []int{2, 3},
		},
		{
			s:        "***|**|*****|**||**|*",
			queries:  [][]int{{1, 17}, {4, 5}, {14, 17}, {5, 11}, {15, 16}},
			expected: []int{9, 0, 0, 0, 0},
		},
		{
			s:        "|||||*|||*|||*||||*||||**|*|||**|**||**|||*|||*||***||*|*||",
			queries:  [][]int{{7, 52}, {1, 55}, {4, 52}, {0, 56}, {28, 34}, {6, 56}, {19, 56}, {1, 29}, {2, 54}, {6, 52}, {1, 58}, {2, 58}, {8, 50}, {5, 51}, {6, 55}, {2, 51}, {18, 39}, {3, 51}, {4, 54}, {5, 58}, {6, 54}, {8, 58}, {16, 55}, {3, 58}, {5, 52}, {0, 53}, {6, 53}, {11, 18}, {0, 51}, {6, 57}, {0, 9}, {8, 53}, {5, 53}, {4, 58}, {2, 53}, {7, 55}, {2, 55}, {6, 58}, {1, 57}, {6, 50}, {5, 50}, {7, 20}, {3, 50}, {3, 52}, {7, 53}, {7, 51}, {5, 39}, {0, 50}, {5, 54}, {1, 52}, {0, 57}, {6, 11}, {2, 50}, {4, 51}, {1, 50}, {30, 49}, {11, 51}, {7, 54}, {8, 51}},
			expected: []int{17, 19, 18, 19, 2, 18, 15, 7, 18, 17, 20, 20, 14, 14, 18, 15, 9, 15, 18, 19, 17, 19, 16, 20, 17, 18, 17, 1, 15, 19, 1, 17, 17, 20, 18, 18, 19, 19, 20, 14, 14, 3, 15, 18, 17, 14, 12, 15, 17, 18, 20, 1, 15, 15, 15, 6, 13, 17, 14},
		},
		{
			s:        "|||||*|||*|||*||||*||||**|*|||**|**||**|||*|||*||***||*|*||",
			queries:  [][]int{{0, 56}},
			expected: []int{19},
		},
	}

	for _, tc := range cases {
		t.Run("", func(t *testing.T) {
			got := platesBetweenCandles(tc.s, tc.queries)
			assert.Equal(t, tc.expected, got)
		})
	}
}
